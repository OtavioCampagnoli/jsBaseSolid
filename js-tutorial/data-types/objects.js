// JavaScript Objects

// JavaScript objects are written with curly braces {}.

// Object properties are written as name:value pairs, separated by commas.

const person = {
  firstName: "Otavio",
  lastName: "Campagnoli",
  age: 20,
  eyeColor: "brown",
};

console.log(
  person.firstName +
    " " +
    person.lastName +
    " " +
    person.age +
    " " +
    person.eyeColor
); //Otavio Campagnoli 20 brown
