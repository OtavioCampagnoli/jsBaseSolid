// JavaScript Assignment Operators

// Assignment operators assign values to JavaScript variables.

// The Addition Assignment Operator (+=) adds a value to a variable.

// Operator 	Example 	Same As
// = 	        x = y 	    x = y
// += 	        x += y 	    x = x + y
// -= 	        x -= y 	    x = x - y
// *= 	        x *= y 	    x = x * y
// /= 	        x /= y 	    x = x / y
// %= 	        x %= y 	    x = x % y
// **= 	        x **= y     x = x ** y

let x = 10;
console.log(x += 5); // 15
console.log(x -= 5); // 10
console.log(x *= 5); // 50
console.log(x /= 5); // 2
console.log(x %= 5); // 0
console.log(x **= 2); // 0
x = 10;
console.log(x **= 2); // 100