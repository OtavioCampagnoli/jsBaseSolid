// JavaScript Logical Operators
// Operator 	Description
// && 	        logical and
// || 	        logical or
// ! 	        logical not

let x = 10;
let y = 20;
let z = 30;
console.log(x == y && z >= 10); // false
console.log(y >= x && z != x); // true
console.log(x == y || z >= 10); // true
console.log(!(x == 10)); //false